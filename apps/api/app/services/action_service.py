SUMMARY_STATUSES = ["Generated", "Approved", "Executed", "Rejected", "Pending Review", "Rolled Back"]
SORT_COLUMNS = {
    "created_at": "o.created_at",
    "confidence_score": "o.confidence_score",
    "expected_impact_pct": "o.expected_impact_pct",
    "actual_impact_pct": "o.actual_impact_pct",
    "status": "o.status",
    "risk_level": "o.risk_level",
}
DEFAULT_AGENTS = ["Data Scout", "Pattern Miner", "Recommendation Engine", "Risk Guardrail", "Action Executor"]

_fallback_events: list[dict[str, Any]] = []
_fallback_actions = [dict(item) for item in ACTIONS]


def now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def number(value: Any) -> float:
    if value is None:
        return 0.0
    if isinstance(value, Decimal):
        return float(value)
    return float(value)


def iso(value: Any) -> Optional[str]:
    if value is None:
        return None
    if hasattr(value, "isoformat"):
        return value.isoformat()
    return str(value)


def parse_json(value: Any, fallback: Any) -> Any:
    if value is None:
        return fallback
    if isinstance(value, (list, dict)):
        return value
    try:
        return json.loads(value)
    except (TypeError, ValueError):
        return fallback


def normalized_status(value: Any) -> str:
    raw = str(value or "Generated").strip().replace("_", " ")
    mapping = {
        "generated": "Generated",
        "pending": "Pending Review",
        "pending review": "Pending Review",
        "approved": "Approved",
        "executed": "Executed",
        "rejected": "Rejected",
        "rolled back": "Rolled Back",
        "rolledback": "Rolled Back",
    }
    return mapping.get(raw.lower(), raw.title())


def action_item(row: dict[str, Any]) -> dict[str, Any]:
    evidence = parse_json(row.get("evidence_refs"), [])
    if not isinstance(evidence, list):
        evidence = [str(evidence)]
    status = normalized_status(row.get("status"))
    return {
        "optimization_id": row.get("optimization_id") or row.get("id"),
        "client_id": row.get("client_id") or row.get("recommendation_id") or "demo-client",
        "brand_name": row.get("brand_name") or row.get("client_name") or "Demo Brand",
        "brand_category": row.get("brand_category"),
        "created_at": iso(row.get("created_at") or row.get("executed_at")) or now_iso(),
        "agent_name": row.get("agent_name") or ("Action Executor" if status in {"Executed", "Rolled Back"} else "Recommendation Engine"),
        "action_type": row.get("action_type") or row.get("type") or row.get("title") or "Optimization",
        "target_platform": row.get("target_platform") or row.get("platform") or infer_platform(row),
        "target_campaign_id": row.get("target_campaign_id") or row.get("campaign_id"),
        "campaign_name": row.get("campaign_name") or row.get("target_campaign_id") or "Campaign configuration",
        "reason": row.get("reason") or row.get("title") or row.get("rollback_reason") or "Optimization action generated by WasteNot.",
        "expected_impact_pct": number(row.get("expected_impact_pct", 0.08)),
        "confidence_score": number(row.get("confidence_score", row.get("confidence", 0.78))),
        "risk_level": str(row.get("risk_level") or row.get("risk") or "Medium").capitalize(),
        "status": status,
        "approved_by": row.get("approved_by") or ("human_ops" if status in {"Approved", "Executed", "Rolled Back"} else None),
        "actual_impact_pct": number(row.get("actual_impact_pct", 0)) if row.get("actual_impact_pct") is not None else None,
        "rollback_flag": bool(row.get("rollback_flag") or status == "Rolled Back" or row.get("rollback_status") == "rolled_back"),
        "evidence_refs": evidence,
        "recommendation_id": row.get("recommendation_id"),
    }


def infer_platform(row: dict[str, Any]) -> str:
    text = " ".join(str(row.get(key, "")) for key in ("title", "type", "reason")).lower()
    if "google" in text or "shopping" in text or "search" in text:
        return "Google"
    return "Meta"


def mock_action_rows() -> list[dict[str, Any]]:
    return [action_item(item) for item in _fallback_actions]


def get_summary() -> dict[str, Any]:
    if is_db_configured():
        try:
            row = fetch_one(
                """
                SELECT COUNT(*) AS total_actions,
                       COUNT(*) FILTER (WHERE lower(status) = 'generated') AS generated,
                       COUNT(*) FILTER (WHERE lower(status) = 'approved') AS approved,
                       COUNT(*) FILTER (WHERE lower(status) = 'executed') AS executed,
                       COUNT(*) FILTER (WHERE lower(status) = 'rejected') AS rejected,
                       COUNT(*) FILTER (WHERE lower(status) IN ('pending review', 'pending_review', 'pending')) AS pending_review,
                       COUNT(*) FILTER (WHERE rollback_flag IS TRUE OR lower(status) IN ('rolled back', 'rolled_back')) AS rolled_back,
                       AVG(confidence_score) AS avg_confidence,
                       AVG(expected_impact_pct) AS estimated_avg_impact_pct,
                       AVG(actual_impact_pct) FILTER (WHERE actual_impact_pct IS NOT NULL) AS actual_avg_impact_pct
                FROM optimization_history
                """
            ) or {}
            return summary_payload(row, "database")
        except Exception:
            pass
    items = mock_action_rows()
    row = {"total_actions": len(items)}
    for status in SUMMARY_STATUSES:
        row[status.lower().replace(" ", "_")] = len([item for item in items if item["status"] == status])
    row["rolled_back"] = len([item for item in items if item["rollback_flag"] or item["status"] == "Rolled Back"])
    row["avg_confidence"] = average([item["confidence_score"] for item in items])
    row["estimated_avg_impact_pct"] = average([item["expected_impact_pct"] for item in items])
    row["actual_avg_impact_pct"] = average([item["actual_impact_pct"] for item in items if item["actual_impact_pct"] is not None])
    return summary_payload(row, "mock")


def summary_payload(row: dict[str, Any], source: str) -> dict[str, Any]:
    total = int(row.get("total_actions") or 0)
    rolled_back = int(row.get("rolled_back") or 0)
    return {
        "total_actions": total,
        "generated": int(row.get("generated") or 0),
        "approved": int(row.get("approved") or 0),
        "executed": int(row.get("executed") or 0),
        "rejected": int(row.get("rejected") or 0),
        "pending_review": int(row.get("pending_review") or 0),
        "rolled_back": rolled_back,
        "rollback_rate": rolled_back / total if total else 0,
        "avg_confidence": number(row.get("avg_confidence")),
        "estimated_avg_impact_pct": number(row.get("estimated_avg_impact_pct")),
        "actual_avg_impact_pct": number(row.get("actual_avg_impact_pct")),
        "source": source,
    }


def average(values: list[float]) -> float:
    return sum(values) / len(values) if values else 0


def filters_sql(filters: dict[str, Any]) -> tuple[str, list[Any]]:
    clauses: list[str] = []
    params: list[Any] = []
    mapping = {
        "client_id": "o.client_id = %s",
        "platform": "o.target_platform = %s",
        "risk_level": "o.risk_level = %s",
        "status": "o.status = %s",
        "agent_name": "o.agent_name = %s",
    }
    for key, clause in mapping.items():
        if filters.get(key):
            clauses.append(clause)
            params.append(filters[key])
    if filters.get("rollback_flag") is not None:
        clauses.append("o.rollback_flag = %s")
        params.append(bool(filters["rollback_flag"]))
    if filters.get("search"):
        clauses.append(
            "(o.action_type ILIKE %s OR o.reason ILIKE %s OR c.brand_name ILIKE %s OR o.target_platform ILIKE %s OR o.target_campaign_id ILIKE %s OR o.agent_name ILIKE %s OR o.approved_by ILIKE %s)"
        )
        params.extend([f"%{filters['search']}%"] * 7)
    return (f"WHERE {' AND '.join(clauses)}" if clauses else "", params)


def list_actions(filters: dict[str, Any]) -> dict[str, Any]:
    limit = int(filters.get("limit") or 50)
    offset = int(filters.get("offset") or 0)
    if is_db_configured():
        try:
            where, params = filters_sql(filters)
            sort_by = filters.get("sort_by") if filters.get("sort_by") in SORT_COLUMNS else "created_at"
            sort_dir = "ASC" if str(filters.get("sort_dir")).lower() == "asc" else "DESC"
            rows = fetch_all(
                f"""
                SELECT o.*, c.brand_name, c.brand_category, s.campaign_name
                FROM optimization_history o
                LEFT JOIN clients c ON c.client_id = o.client_id
                LEFT JOIN ad_campaign_settings s ON s.campaign_id = o.target_campaign_id
                {where}
                ORDER BY {SORT_COLUMNS[sort_by]} {sort_dir} NULLS LAST
                LIMIT %s OFFSET %s
                """,
                tuple(params + [limit, offset]),
            )
            total = fetch_one(
                f"""
                SELECT COUNT(*) AS total
                FROM optimization_history o
                LEFT JOIN clients c ON c.client_id = o.client_id
                {where}
                """,
                tuple(params),
            ) or {}
            return {"items": [action_item(row) for row in rows], "total": int(total.get("total") or 0), "limit": limit, "offset": offset, "source": "database"}
        except Exception:
            pass
    items = filter_mock_actions(filters)
    sort_by = filters.get("sort_by") or "created_at"
    reverse = str(filters.get("sort_dir") or "desc").lower() != "asc"
    items.sort(key=lambda item: sort_value(item.get(sort_by)), reverse=reverse)
    return {"items": items[offset : offset + limit], "total": len(items), "limit": limit, "offset": offset, "source": "mock"}


def sort_value(value: Any) -> tuple[int, Any]:
    if value is None:
        return (1, "")
    return (0, value)


def filter_mock_actions(filters: dict[str, Any]) -> list[dict[str, Any]]:
    items = mock_action_rows()
    for key, field in [("client_id", "client_id"), ("platform", "target_platform"), ("risk_level", "risk_level"), ("status", "status"), ("agent_name", "agent_name")]:
        if filters.get(key):
            items = [item for item in items if item.get(field) == filters[key]]
    if filters.get("rollback_flag") is not None:
        items = [item for item in items if item["rollback_flag"] is bool(filters["rollback_flag"])]
    if filters.get("search"):
        search = str(filters["search"]).lower()
        items = [
            item for item in items
            if search in " ".join(str(item.get(key, "")) for key in ("action_type", "reason", "brand_name", "target_platform", "target_campaign_id", "agent_name", "approved_by")).lower()
        ]
    return items


