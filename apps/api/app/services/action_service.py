def get_action_detail(optimization_id: str) -> dict[str, Any]:
    action = get_action_or_404(optimization_id)
    client: dict[str, Any] = {}
    campaign: dict[str, Any] = {}
    related_recommendation = None
    if is_db_configured():
        try:
            client = fetch_one("SELECT * FROM clients WHERE client_id = %s", (action["client_id"],)) or {}
            if action.get("target_campaign_id"):
                campaign = fetch_one("SELECT * FROM ad_campaign_settings WHERE campaign_id = %s", (action["target_campaign_id"],)) or {}
            related_recommendation = infer_recommendation(action)
        except Exception:
            client = {}
            campaign = {}
    events = get_audit_events({"action_id": optimization_id, "limit": 100})["items"]
    return {
        "action": action,
        "client": client or {"client_id": action["client_id"], "brand_name": action["brand_name"], "brand_category": action.get("brand_category")},
        "campaign": campaign or {"campaign_id": action.get("target_campaign_id"), "campaign_name": action.get("campaign_name"), "platform": action.get("target_platform")},
        "related_recommendation": related_recommendation,
        "audit_events": events,
        "rollback_plan": rollback_plan(action),
        "safety_checks": safety_checks(action),
        "source": "database" if is_db_configured() else "mock",
    }


def get_action_or_404(optimization_id: str) -> dict[str, Any]:
    if is_db_configured():
        try:
            row = fetch_one(
                """
                SELECT o.*, c.brand_name, c.brand_category, s.campaign_name
                FROM optimization_history o
                LEFT JOIN clients c ON c.client_id = o.client_id
                LEFT JOIN ad_campaign_settings s ON s.campaign_id = o.target_campaign_id
                WHERE o.optimization_id = %s
                """,
                (optimization_id,),
            )
            if row:
                return action_item(row)
        except Exception:
            pass
    action = next((item for item in mock_action_rows() if item["optimization_id"] == optimization_id), None)
    if not action:
        raise HTTPException(status_code=404, detail="Optimization action not found")
    return action


def infer_recommendation(action: dict[str, Any]) -> Optional[dict[str, Any]]:
    if not is_db_configured():
        return None
    return fetch_one(
        """
        SELECT *
        FROM recommendation_records
        WHERE client_id = %s
          AND (
            target_campaign_id = %s
            OR lower(recommendation_type) = lower(%s)
            OR lower(target_platform) = lower(%s)
          )
        ORDER BY ABS(EXTRACT(EPOCH FROM (detected_at - %s::timestamptz))) ASC
        LIMIT 1
        """,
        (
            action["client_id"],
            action.get("target_campaign_id"),
            action.get("action_type"),
            action.get("target_platform"),
            action.get("created_at"),
        ),
    )


def rollback_plan(action_or_id: dict[str, Any] | str) -> dict[str, Any]:
    action = action_or_id if isinstance(action_or_id, dict) else get_action_or_404(action_or_id)
    status = action["status"]
    eligible = status in {"Executed", "Approved"}
    blocking_reason = None
    if status in {"Generated", "Pending Review"}:
        blocking_reason = "Action has not been executed yet; rollback is not needed."
    elif status == "Rejected":
        blocking_reason = "Rejected actions are not eligible for rollback."
    elif status == "Rolled Back":
        blocking_reason = "Action has already been rolled back."
    return {
        "rollback_available": eligible,
        "rollback_type": "restore_previous_campaign_or_audience_configuration",
        "summary": "Restore the prior campaign/audience state using the audit log and previous optimization state.",
        "steps": [
            "Confirm current optimization is underperforming against guardrail thresholds.",
            "Retrieve previous configuration snapshot or prior known-good state.",
            "Revert audience/campaign setting in the ad platform.",
            "Monitor CPA, ROAS, spend, and purchases for the next 24-72 hours.",
            "Record rollback outcome in optimization history and agent logs.",
        ],
        "rollback_eligibility": "eligible" if eligible else "not_eligible",
        "blocking_reason": blocking_reason,
    }


def safety_checks(action: dict[str, Any]) -> list[dict[str, Any]]:
    return [
        {"label": "External API calls disabled", "status": "passed", "message": "Demo execution never calls Meta or Google Ads APIs."},
        {"label": "Rollback plan available", "status": "passed" if action["status"] in {"Approved", "Executed", "Rolled Back"} else "pending", "message": "A conceptual rollback path is attached before execution."},
        {"label": "High-risk approval gate", "status": "passed" if action["risk_level"] != "High" or action.get("approved_by") else "blocked", "message": "High-risk changes require human approval."},
        {"label": "Evidence references attached", "status": "passed" if action.get("evidence_refs") else "pending", "message": "Evidence refs support auditability without exposing hidden reasoning."},
    ]


def get_audit_events(filters: dict[str, Any]) -> dict[str, Any]:
    limit = int(filters.get("limit") or 100)
    if is_db_configured():
        try:
            clauses = []
            params: list[Any] = []
            for key in ("action_id", "recommendation_id", "client_id"):
                if filters.get(key):
                    clauses.append(f"{key} = %s")
                    params.append(filters[key])
            where = f"WHERE {' AND '.join(clauses)}" if clauses else ""
            rows = fetch_all(
                f"""
                SELECT audit_id, action_id, recommendation_id, client_id, event_type, event_status, actor, message, metadata, created_at
                FROM action_audit_events
                {where}
                ORDER BY created_at DESC
                LIMIT %s
                """,
                tuple(params + [limit]),
            )
            return {"items": [audit_event(row) for row in rows], "source": "database"}
        except Exception:
            pass
    events = [audit_event(row) for row in _fallback_events]
    for key in ("action_id", "recommendation_id", "client_id"):
        if filters.get(key):
            events = [event for event in events if event.get(key) == filters[key]]
    return {"items": events[:limit], "source": "mock"}


